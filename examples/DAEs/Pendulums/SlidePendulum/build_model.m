function [A,B,Fnl,Fext] = build_model(c1,c2,k1,k2)

m1 = 1;
m2 = 1;
len = 1;
J2 = m2*len^2/12;
g = 9.8;

B = zeros(15);
A = zeros(15);
B(1:5,1:5) = eye(5);
B(6:10,6:10) = diag([m1 m1 m2 m2 J2]);
B(14,14) = 1;
A(1:5,6:10) = eye(5);
A(6,1) = -k1; A(6,6) = -c1; A(6,12) = 1;
A(7,11) = -1; A(7,13) = 1;
A(8,12) = -1;
A(9,13) = -1;
A(10,5) = -k2; A(10,10) = -c2; A(10,12) = 0.5*len; A(10,14) = -0.5*len*m2*g;
A(11,2) = 1;
A(12,1) = -1; A(12,3) = 1; A(12,14) = -0.5*len;
A(13,2) = -1; A(13,4) = 1; A(13,15) = 0.5*len;
A(14,10) = 1;
A(15,15) = -2;

subs2 = [10 12 15
    10 13 14
    14 10 15
    15 14 14
    15 15 15];
vals2 = [-0.5*len; -0.5*len; -1; 1; 1];
F2 = sptensor(subs2, vals2, [15 15 15]);
Fnl = {F2};
Fext = zeros(15,1);
Fext(6) = 1;

end